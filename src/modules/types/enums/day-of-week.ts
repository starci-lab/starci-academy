/**
 * Day of week for recurring livestream slots (`day_of_week` / Nest `DayOfWeek` / GraphQL `DayOfWeek`).
 */
export enum DayOfWeek {
    /** Sunday (first day of the week in ISO 8601 locale variants). */
    Sunday = "sunday",
    /** Monday — first working day of the standard week. */
    Monday = "monday",
    /** Tuesday. */
    Tuesday = "tuesday",
    /** Wednesday — midweek. */
    Wednesday = "wednesday",
    /** Thursday. */
    Thursday = "thursday",
    /** Friday — last standard working day. */
    Friday = "friday",
    /** Saturday. */
    Saturday = "saturday",
}
